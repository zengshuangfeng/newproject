package com.meisui.manage.entity;
/**
 * 
 * 账户收款验证实体类
 * */
public class AccountReceivable {

	private int id;
	private int authentication_id;// 实名认证表id
	private String payment_account;//打款账户
	private String verification_amount;//输入验证金额
	private String payee_name;//收款人姓名
	private String receipt_account;//验证收款账户
	private String payer_name;//付款人姓名
	private String pay_account;//付款账户
	private String pay_number;//验证账户支付金额
	private String pay_type;//对账类型
	private String remark;//备注
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPayee_name() {
		return payee_name;
	}
	public void setPayee_name(String payee_name) {
		this.payee_name = payee_name;
	}
	public String getReceipt_account() {
		return receipt_account;
	}
	public void setReceipt_account(String receipt_account) {
		this.receipt_account = receipt_account;
	}
	public String getPayer_name() {
		return payer_name;
	}
	public void setPayer_name(String payer_name) {
		this.payer_name = payer_name;
	}
	public String getPay_account() {
		return pay_account;
	}
	public void setPay_account(String pay_account) {
		this.pay_account = pay_account;
	}
	public String getPay_number() {
		return pay_number;
	}
	public void setPay_number(String pay_number) {
		this.pay_number = pay_number;
	}
	public String getPay_type() {
		return pay_type;
	}
	public void setPay_type(String pay_type) {
		this.pay_type = pay_type;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getPayment_account() {
		return payment_account;
	}
	public void setPayment_account(String payment_account) {
		this.payment_account = payment_account;
	}
	public String getVerification_amount() {
		return verification_amount;
	}
	public void setVerification_amount(String verification_amount) {
		this.verification_amount = verification_amount;
	}
	public int getAuthentication_id() {
		return authentication_id;
	}
	public void setAuthentication_id(int authentication_id) {
		this.authentication_id = authentication_id;
	}
	
}
