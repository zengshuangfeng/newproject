public class AdmissionTicket {
	
	private String nickname;
	private long uid;
	private Date start_time;
	private Date end_time;
	private String fee_count;
	private String single_total_time;
	/**
	 * @return the nickname
	 */
	public String getNickname() {
		return nickname;
	}
	/**
	 * @param nickname the nickname to set
	 */
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	/**
	 * @return the uid
	 */
	public long getUid() {
		return uid;
	}
	/**
	 * @param uid the uid to set
	 */
	public void setUid(long uid) {
		this.uid = uid;
	}
	/**
	 * @return the start_time
	 */
	public Date getStart_time() {
		return start_time;
	}
	/**
	 * @param start_time the start_time to set
	 */
	public void setStart_time(Date start_time) {
		this.start_time = start_time;
	}
	/**
	 * @return the end_time
	 */
	public Date getEnd_time() {
		return end_time;
	}
	/**
	 * @param end_time the end_time to set
	 */
	public void setEnd_time(Date end_time) {
		this.end_time = end_time;
	}
	/**
	 * @return the fee_count
	 */
	public String getFee_count() {
		return fee_count;
	}
	/**
	 * @param fee_count the fee_count to set
	 */
	public void setFee_count(String fee_count) {
		this.fee_count = fee_count;
	}
	/**
	 * @return the single_total_time
	 */
	public String getSingle_total_time() {
		return single_total_time;
	}
	/**
	 * @param single_total_time the single_total_time to set
	 */
	public void setSingle_total_time(String single_total_time) {
		this.single_total_time = single_total_time;
	}
}
