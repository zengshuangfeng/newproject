package com.meisui.manage.entity;

public class PublicEntity {
	private int invite_Count; // 邀请总人数
	private long invite_Total_Recharge;// 邀请总充值
	private long invite_Total_Give;// 邀请总赠送
	private long live_Total_Glamour;// 直播所得总魅力值
	private long spread_Total_Glamour;// 推广所得总魅力值

	public int getInvite_Count() {
		return invite_Count;
	}

	public void setInvite_Count(int invite_Count) {
		this.invite_Count = invite_Count;
	}

	public long getInvite_Total_Recharge() {
		return invite_Total_Recharge;
	}

	public void setInvite_Total_Recharge(long invite_Total_Recharge) {
		this.invite_Total_Recharge = invite_Total_Recharge;
	}

	public long getInvite_Total_Give() {
		return invite_Total_Give;
	}

	public void setInvite_Total_Give(long invite_Total_Give) {
		this.invite_Total_Give = invite_Total_Give;
	}

	public long getLive_Total_Glamour() {
		return live_Total_Glamour;
	}

	public void setLive_Total_Glamour(long live_Total_Glamour) {
		this.live_Total_Glamour = live_Total_Glamour;
	}

	public long getSpread_Total_Glamour() {
		return spread_Total_Glamour;
	}

	public void setSpread_Total_Glamour(long spread_Total_Glamour) {
		this.spread_Total_Glamour = spread_Total_Glamour;
	}

}
