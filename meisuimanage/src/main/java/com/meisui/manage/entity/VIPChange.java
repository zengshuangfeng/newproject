public class VIPChange {

	private int id;
	private String name;
	private int days;
	private int change_rmb;
	private int is_online;
	private String w_name;
	private String update_time;
	private String create_time;
	
	
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the days
	 */
	public int getDays() {
		return days;
	}
	/**
	 * @param days the days to set
	 */
	public void setDays(int days) {
		this.days = days;
	}
	/**
	 * @return the change_rmb
	 */
	public int getChange_rmb() {
		return change_rmb;
	}
	/**
	 * @param change_rmb the change_rmb to set
	 */
	public void setChange_rmb(int change_rmb) {
		this.change_rmb = change_rmb;
	}
	
	/**
	 * @return the is_online
	 */
	public int getIs_online() {
		return is_online;
	}
	/**
	 * @param is_online the is_online to set
	 */
	public void setIs_online(int is_online) {
		this.is_online = is_online;
	}
	/**
	 * @return the w_name
	 */
	public String getW_name() {
		return w_name;
	}
	/**
	 * @param w_name the w_name to set
	 */
	public void setW_name(String w_name) {
		this.w_name = w_name;
	}
	/**
	 * @return the update_time
	 */
	public String getUpdate_time() {
		return update_time;
	}
	/**
	 * @param update_time the update_time to set
	 */
	public void setUpdate_time(Date update_time) {
		this.update_time = AuthUtil.formatDateToString(update_time);
	}
	/**
	 * @return the create_time
	 */
	public String getCreate_time() {
		return create_time;
	}
	/**
	 * @param create_time the create_time to set
	 */
	public void setCreate_time(Date create_time) {
		this.create_time = AuthUtil.formatDateToString(create_time);
	}
}
