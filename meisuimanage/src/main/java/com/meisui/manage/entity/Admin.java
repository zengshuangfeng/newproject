package com.meisui.manage.entity;

public class Admin {
	private int id ;//人员ID
	private String nickname;//人员昵称
	private String username;//用户名
	private String password;//密码
	private String remark;//备注
	private int group;//角色id
	private String icon;//图标
	private String im;
	private int type;//类型 0内部操作人员 1视频审核人员
	private int is_close;//是否关闭权限 0否 1是
	private int video_count;//审核视频数量
	private String w_name;//编辑人员
	private int is_read;//0主账号，1子账号
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public int getGroup() {
		return group;
	}
	public void setGroup(int group) {
		this.group = group;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getIm() {
		return im;
	}
	public void setIm(String im) {
		this.im = im;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getIs_close() {
		return is_close;
	}
	public void setIs_close(int is_close) {
		this.is_close = is_close;
	}
	public int getVideo_count() {
		return video_count;
	}
	public void setVideo_count(int video_count) {
		this.video_count = video_count;
	}
	public String getW_name() {
		return w_name;
	}
	public void setW_name(String w_name) {
		this.w_name = w_name;
	}
	public int getIs_read() {
		return is_read;
	}
	public void setIs_read(int is_read) {
		this.is_read = is_read;
	}
	
}
