public class Anchor_Virtual_Rule {
	private int max_virtual;//最大魅力数
	private int min_virtual;//最小魅力数
	private int rank;//档位数
	private int base_salary;//底薪
	private int divide_proportion;//分成比例
	private int effective_days;//有效天数
	public int getMax_virtual() {
		return max_virtual;
	}
	public void setMax_virtual(int max_virtual) {
		this.max_virtual = max_virtual;
	}
	public int getMin_virtual() {
		return min_virtual;
	}
	public void setMin_virtual(int min_virtual) {
		this.min_virtual = min_virtual;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public int getBase_salary() {
		return base_salary;
	}
	public void setBase_salary(int base_salary) {
		this.base_salary = base_salary;
	}
	public int getDivide_proportion() {
		return divide_proportion;
	}
	public void setDivide_proportion(int divide_proportion) {
		this.divide_proportion = divide_proportion;
	}
	public int getEffective_days() {
		return effective_days;
	}
	public void setEffective_days(int effective_days) {
		this.effective_days = effective_days;
	}
}
